"""
Module for basic arithmetic operations.

This module provides functions to perform basic arithmetic operations,
including addition of two numbers.
"""


def add_two_numbers(num1: int, num2: int) -> int:
    """
    Add two integers and return the result.

    Args:
        num1 (int): The first integer to add.
        num2 (int): The second integer to add.

    Returns:
        int: The sum of num1 and num2.
    """
    return num1 + num2
